mood = input ("How do you feel today?: ")


if mood == "happy":
    print ("It is great to see you happy!")
elif mood == "nervous":
    print ("Take a deep breath 3 times")
elif mood == "sad":
    print ("Cheer up!")
elif mood == "excited":
    print ("It is great to see you like that")
elif mood == "relaxed":
    print ("Would you like to have a drink?")
else:
    print ("I don't recognize this mood")
